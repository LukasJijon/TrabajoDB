    public void listarInformacion() 
    {
        Connection cn = null;
        try 
        {
          // Par치metros de conexi칩n
          String driver = "oracle.jdbc.OracleDriver";
          String url = "jdbc:oracle:thin:@localhost:1521:XE";
          String user = "user1";
          String pwd = "user1";
          // Conexi칩n
          Class.forName(driver).newInstance();
          cn = DriverManager.getConnection(url, user, pwd);
          // Proceso
          String sql = "{call INFORMACIONCURSOR(?)}";
          CallableStatement cstm = cn.prepareCall(sql);
          cstm.registerOutParameter(1, OracleTypes.CURSOR);
          cstm.executeUpdate();
          ResultSet rs = (ResultSet) cstm.getObject(1);
          String [] titulos={"Numero propiedad","Estado","Tipo","Nombre propietario","Nombre empleado"};

            model= new DefaultTableModel(null,titulos);
            String []fila=new String[5];
            while(rs.next())
            {
                fila[0]=rs.getString("numero");
                fila[1]=rs.getString("estados");
                fila[2]=rs.getString("tipos");
                fila[3]=rs.getString("nombrepropie");
                fila[4]=rs.getString("nombreemple");
                model.addRow(fila);
            }
            tablePropi.setModel(model);
          rs.close();
          cstm.close();
        } 
        catch (Exception e) 
        {
          e.printStackTrace();
        } 
        finally
        {
          try 
          {
            cn.close();
          } 
          catch (Exception e) 
          {
          }
        }
    }
    
